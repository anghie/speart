    /**
     * Nivel de desarrollo 5 Altamente desarrollada 4 Desarrollada 3
     * Medianamente desarrollada 2 Poco desarrollada 1 no desarrollada
     */
    class GestorControladorCompTecn implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            if (e.getSource() == cbRelevanciaTec) {
                cct.comportaObservTec(cbRelevanciaTec.getSelectedIndex(), getCt());
            } else if (e.getSource() == cbNivelDesarr) {
                if (ControladorEvaluacion.facCompTec > 0) {
                    cct.actualizaResCompTec();
                } else {
                    JOptionPane.showMessageDialog(null, "Debe llenar el campo de factor de competencia tecnica con valores mayores a cero");
                }
            }
        }
    }


