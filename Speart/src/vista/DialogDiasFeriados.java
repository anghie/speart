    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
       
       Calendar calendario=fechaFeriado.getSelectedDate();
       if(agenda.getNombre().equals(""+calendario.get(Calendar.YEAR))){
            String mes= Fecha.getNombreMes(calendario.get(Calendar.MONTH));
            String dia= Fecha.getNombreDia(calendario.get(Calendar.DAY_OF_WEEK));
            
            DiaFeriado diaFeriado=new DiaFeriado();
            diaFeriado.setAgenda(agenda);
            diaFeriado.setMes(mes);
            diaFeriado.setDia(dia);
            diaFeriado.setDia_mes(calendario.get(Calendar.DATE));
            diaFeriado.setDetalle(txtDetalle.getText());
            if(!agenda.getDiasFeriados().contains(diaFeriado)){
                agenda.getDiasFeriados().add(diaFeriado);
                modeloTabla.add(diaFeriado);
                this.guardar=true;
                tablaDiasferiados.revalidate();
            }else{
                JOptionPane.showMessageDialog(this,"Este feriado ya fue agregado");
            }         
            
       }else {
           JOptionPane.showMessageDialog(this,"No sepuede agregar el feriado a la agenda aÃ±os diferentes");
       }
       
       
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int index=tablaDiasferiados.getSelectedRow();
        if(index>-1){
            
            DiaFeriado diaFeriado=modeloTabla.getDiaFeriado(index);
            agenda.getDiasFeriados().remove(diaFeriado);
            modeloTabla.remov(index);
            tablaDiasferiados.revalidate();
            this.guardar=true;
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

