    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        // TODO add your handling code here:
        if(cmbServidor.getSelectedIndex()>-1){
            Actividad actividad= ((ModeloComboBoxActividad)cmbServidor.getModel()).getSelectedActividad();
            itemAgenda.setActividad(actividad);
            itemAgenda.setNombre_actividad(actividad.getNombreActividad());
        }
        itemAgenda.setPorcentaje(Double.parseDouble(spnPorcentaje.getValue().toString()));
        itemAgenda.setObservacion(txtObservacion.getText());
        itemAgenda.getHoraInicio().setHours(Integer.parseInt(spHoraInicio.getValue().toString()));
        itemAgenda.getHoraInicio().setMinutes(Integer.parseInt(spMinutosInicio.getValue().toString()));

        itemAgenda.getHoraFin().setHours(Integer.parseInt(spHoraFinal.getValue().toString()));
        itemAgenda.getHoraFin().setMinutes(Integer.parseInt(spMinutosFinal.getValue().toString()));

        double h=Integer.parseInt(spHoraFinal.getValue().toString())-Integer.parseInt(spHoraInicio.getValue().toString());
        double m=Integer.parseInt(spMinutosFinal.getValue().toString())-Integer.parseInt(spMinutosInicio.getValue().toString());
        m=h*60+m;

        h=m/60;
        m=(h-(int)h)*60;
        m=Math.round(m);
        ControladorItemAgenda.setTiempoDuracion(itemAgenda,
                                                (int)h,
                                                (int)m);

        itemAgenda.setRecordar(chbRecordar.isSelected());
        dispose();
    }//GEN-LAST:event_btAceptarActionPerformed

